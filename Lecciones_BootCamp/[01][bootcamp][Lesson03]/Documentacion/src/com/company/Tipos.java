package com.company;

public class Tipos {
    public static void main(String[] args) {

        //tipo identificador = <<valor>>;
        //otra forma es:
        //tipo identificador;
        //identificador = <<valor>>;

        //Enteros
        byte number1 = 1; //1 byte
        short number2 = 2; //2 byte
        int number3 = 3; //3 byte
        long number4 = 4; //4 byte

        //punto flotante
        float numero1 = 4.9f;
        double numero2 = 4.9d;

        //caracter
        char caracter1 = 'a';

        //boleanos
        boolean verdadero = true;
        boolean falso = false;

        //cadena de textos
        String nombre = "Ronald";
        String apellido = "Melara";

        //tipos envoltorios: permite manejar nulos
        Integer numero3 = null;
        Long numero4 = 2L;
    }
}
